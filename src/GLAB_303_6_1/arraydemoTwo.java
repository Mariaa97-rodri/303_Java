package GLAB_303_6_1;

public class arraydemoTwo {
    public static void main(String[] args) {

        //create an array
        int [] age = {12, 4, 5};

        //Loop through an array
        //using for loop

        System.out.println("Using for Loop:");
        for(int i = 0; i < age.length; i++) {
            System.out.println(age[i]);
        }

    }
}
